char firstSwitch(char c) {
	return switchingArr[c];
}

int firstFastRotorEncoding(char c) {
	for (int i = 0; i < ALPHABET_LEN; i++) {
		if (c == fastRotorArr[i]) return i;
	}
}

int firstMiddleRotorEncoding(int number) {
	for (int i = 0; i < ALPHABET_LEN; i++) {
		if (number == middleRotorArr[i]) return i;
	}
}

char firstSlowRotorEncoding(int index) {
	return slowRotorArr[index];
}

char getReflectedChar(char c) {
	for (int i = 0; i < ALPHABET_LEN; i++) {
		if (reflectorArr1[i] == c) return reflectorArr2[i];
	}
}

int secondSlowRotorEncoding(char c) {
	for (int i = 0; i < ALPHABET_LEN; i++) {
		if (c == slowRotorArr[i]) return i;
	}
}

int secondMiddleRotorEncoding(int index) {
	return middleRotorArr[index];
}

char secondFastRotorEncoding(int index) {
	return fastRotorArr[index];
}

void scrollMiddleRotor() {
	int tmp = fastRotorArr[0];
	for (int i = 0; i < ALPHABET_LEN - 1; i++) {
		middleRotorArr[i] = middleRotorArr[i + 1];
	}
	middleRotorArr[ALPHABET_LEN - 1] = tmp;
}

char secondSwitch(char c) {
	for (int i = 0; i < ALPHABET_LEN; i++) {
		if (switchingArr[i] == c) return i;
	}
}

void scrollFastRotor() {
	char tmp = fastRotorArr[0];
	for (int i = 0; i < ALPHABET_LEN - 1; i++) {
		fastRotorArr[i] = fastRotorArr[i + 1];
	}
	fastRotorArr[ALPHABET_LEN - 1] = tmp;
}


void scrollSlowRotor() {
	char tmp = slowRotorArr[0];
	for (int i = 0; i < ALPHABET_LEN - 1; i++) {
		slowRotorArr[i] = slowRotorArr[i + 1];
	}
	slowRotorArr[ALPHABET_LEN - 1] = tmp;
}

int main(int argc, char* argv[]) { 
	int stringSize;
	char *string;

	FILE *f = fopen("input.txt", "rb");
	FILE *fo = fopen("output.txt", "wb");
	if (!f) {
		printf("Не удалось открыть файл!");
		return 1;
	}

	if (fscanf(f, "%d\n", &stringSize) != 1) {
		printf("Не удалось считать число символов!");
		return 1;
	}
	
	string = malloc(stringSize); 
	for (int i = 0; i < stringSize; i++) {
		if (fscanf(f, "%c", &string[i]) != 1) {
			printf("Не удалось считать символ!");
			return 1;
		}
	}
	
	int scrollCounter = 0;
	for (int i = 0; i < stringSize; i++) {
		if (string[i] != ' ') {
			string[i] = secondSwitch(
				secondFastRotorEncoding(
				secondMiddleRotorEncoding(
				secondSlowRotorEncoding(
				getReflectedChar(
				firstSlowRotorEncoding(
				firstMiddleRotorEncoding(
				firstFastRotorEncoding(
				firstSwitch(string[i])
				)))))))); 
			scrollCounter++;
			scrollFastRotor();
			if (scrollCounter % 26 == 0)
				scrollMiddleRotor();
			if (scrollCounter % (26 * 26) == 0)
				scrollSlowRotor();
		}
	}

	for (int i = 0; i < stringSize; i++) fprintf(fo, "%c", string[i]);
	free(string);
}
